; Check for key press
defm            check_key
                lda $cb  ; Current key pressed
                cmp #/1
                bne @not_pressed
                lda #1
                jmp @check_key_done
@not_pressed    lda #0
@check_key_done nop
endm

; Compare a 16 bit number for equality to another 16 bit number
defm            compare_numbers
                ; Check MSBs first
                lda /2
                cmp /4
                bcs @eq_gt1
                ; MSB 1 < MSB 2
                lda #2
                jmp @check_done
@eq_gt1         bne @gt
                ; MSBs are equal, now compare LSBs
                lda /1
                cmp /3
                bcs @eq_gt2
                ; MSB 1 = MSB 2, LSB 1 < LSB 2
                lda #2
                jmp @check_done
@eq_gt2         bne @gt
                ; MSBs and LSBs are equal
                lda #0
                jmp @check_done
@gt             lda #1
@check_done     nop
endm

; Decrement a 16 bit number
defm            decrement_number
                lda /1
                bne @skip
                dec /1 + 1
@skip           dec /1
endm

; Increment a 16 bit number
defm            increment_number
                lda /1
                cmp #255
                bne @skip
                inc /1 + 1
@skip           inc /1
endm

